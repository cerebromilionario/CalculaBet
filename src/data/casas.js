export const activePartners = casas
  .filter(partner => partner.active)
  .sort((a, b) => a.priority - b.priority);

export const parceiroDestaque = activePartners[0];

export function getPartnerById(id) {
  return activePartners.find(partner => partner.id === id);
}

export function stablePartnerIndex(seed = '') {
  return String(seed).split('').reduce((acc, char) => acc + char.charCodeAt(0), 0) % activePartners.length;
}

export function getPartnersForPlacement(seed = 'default', count = 2) {
  if (activePartners.length <= count) return activePartners;

  const start = stablePartnerIndex(seed);
  const partners = [];

  for (let offset = 0; partners.length < count && offset < activePartners.length; offset += 1) {
    const partner = activePartners[(start + offset) % activePartners.length];
    if (!partners.some(item => item.id === partner.id)) partners.push(partner);
  }

  return partners;
}

export function getPreferredBanner(partner, preferredSizes = []) {
  if (!partner?.banners) return null;

  const preferred = preferredSizes.find(size => partner.banners[size]);
  if (preferred) return { ...partner.banners[preferred], size: preferred };

  const [fallbackSize] = Object.keys(partner.banners);
  return fallbackSize ? { ...partner.banners[fallbackSize], size: fallbackSize } : null;
}

